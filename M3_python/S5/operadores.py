#operadores logicos
#and, or, not

a = True
b = False

print(a and b) #False, se tienen que cumplir las dos condiciones en True
print(a or b) #True, se tiene que cumplir una de las dos condiciones en True
print(not a) #False, cambia el valor de la variable
print(not (a and b)) #True
print(not (a or b)) #False


# operadores de comparacion
# retornan un booleano True or False
# ==, !=, <, >, <=, >=

c = 5
d = 10

print(c == d) #False
print(c != d) #True
print(c < d) #True
print(c > d) #False
print(c <= d) #True
print(c >= d) #False
