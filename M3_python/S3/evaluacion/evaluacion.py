# importar libreria math
# import math as matematicas
import math

# asignacion de variables
y = 81

# proceso o procedimiento a realizar
raiz_cuadrada = math.sqrt(y)

# print de resultados
print(f'La raiz cuadrada de {y} es: {raiz_cuadrada}')

cadena = f'La raiz cuadrada de {y} es: {raiz_cuadrada:.2f}'
print(cadena)